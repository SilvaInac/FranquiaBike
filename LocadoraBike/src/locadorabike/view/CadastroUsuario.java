    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        
        //Nome deve conter no minimo 6 caracteres
        if(campoNome.getText().length() < 6){
            JOptionPane.showMessageDialog(this, "O nome deve ter no minimo 6 caracteres\n Atualmente esta com: " + campoNome.getText().length() + " Caracteres", "Nome InvÃ¡lido", 2);
        }
        //CPF nÃ£o pode conter pontos ou traÃ§Ãµs e dever conter 11 caracteres
        else if(campoCPF.getText().contains(".")){
            JOptionPane.showMessageDialog(this, "O CPF nÃ£o deve conter ponto(.)", "CPF InvÃ¡lido", 2);
        }
        else if(campoCPF.getText().contains("-")){
            JOptionPane.showMessageDialog(this, "O CPF nÃ£o deve conter traÃ§o(-)", "CPF InvÃ¡lido", 2);
        }
        else if(campoCPF.getText().length() != 11){
            JOptionPane.showMessageDialog(this, "O CPF deve conter 11 caracteres\n Atualmente esta com: " + campoCPF.getText().length() + " Caracteres", "CPF InvÃ¡lido", 2);
        }
        //telefone nÃ£o pode conter parenteses ou traÃ§os e 11 digitos
        else if(campoTelefone.getText().contains("(") || campoTelefone.getText().contains(")")){
            JOptionPane.showMessageDialog(this, "O Telefone nÃ£o deve conter parenteses'( )'", "Telefone InvÃ¡lido", 2);
        }
        else if(campoTelefone.getText().contains("-")){
            JOptionPane.showMessageDialog(this, "O Telefone nÃ£o deve conter traÃ§o(-)", "CPF InvÃ¡lido", 2);
        }
        else if(campoTelefone.getText().length() != 11){
            JOptionPane.showMessageDialog(this, "O Telefone deve conter 11 digitos\n Atualmente esta com: " + campoTelefone.getText().length() + " Digitos", "Telefone InvÃ¡lido", 2);
        }
        //Senha deve conter 6 caracteres ou mais
        else if(campoSenha.getPassword().length < 6){
            JOptionPane.showMessageDialog(this, "A senha deve no minimo 8 caracteres\n Atualmente esta com: " + campoSenha.getPassword().length + " Caracteres", "Senha InvÃ¡lido", 2);
        }
        //verifica se o usuario colocou a senha correta
        else if(!new String(campoSenha.getPassword()).equals(new String(campoConfirmarSenha.getPassword()))){
            JOptionPane.showMessageDialog(this, "As senhas devem ser iguais", "Senha InvÃ¡lido", 2);
        }
        else{
            Usuario user = new Usuario();
            user.nome = campoNome.getText();
            user.cpf = Integer.parseInt(campoCPF.getText());
            user.endereco = campoEndereco.getText();
            user.telefone = Integer.parseInt(campoTelefone.getText());
            user.senha = new String(campoSenha.getPassword());
            
        }
        
    }//GEN-LAST:event_botaoCadastrarActionPerformed

